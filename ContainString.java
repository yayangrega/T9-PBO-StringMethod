public class ContainString {
    
    public static void main(String[] args) {
        String name = "Welcome to Yayang Rega Abdilah";
        System.out.println("Hasilnya : " +name.contains("Yayang Rega Abdilah"));
        System.out.println("Hasilnya : " +name.contains("is"));
        System.out.println("Hasilnya : " +name.contains("back"));
    }
}
