public class endsWithString {
    
    public static void main(String[] args) {
        String string1 = "Hello are you healthy";
        System.out.println("Hasilnya " +string1.endsWith("healthy"));
        System.out.println("Hasilnya " +string1.endsWith("e"));
        System.out.println("Hasilnya " +string1.endsWith("r"));
        System.out.println("Hasilnya " +string1.endsWith("H"));
    }
}
